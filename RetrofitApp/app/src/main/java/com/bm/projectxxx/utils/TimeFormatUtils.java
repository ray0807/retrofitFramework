package com.bm.projectxxx.utils;

import android.content.Context;
import android.text.format.DateUtils;
import android.text.format.Time;

public class TimeFormatUtils {
	public static String formatTimeStampString(Context context, long when) {
		return formatTimeStampString(context, when, false);
	}

	public static String formatTimeStampString(Context context, long when, boolean fullFormat) {
		Time then = new Time();
		then.set(when);
		Time now = new Time();
		now.setToNow();

		// Basic settings for formatDateTime() we want for all cases.
		int format_flags = DateUtils.FORMAT_NO_NOON_MIDNIGHT | DateUtils.FORMAT_ABBREV_ALL | DateUtils.FORMAT_CAP_AMPM;

		// If the message is from a different year, show the date and year.
		if (then.year != now.year) {
			format_flags |= DateUtils.FORMAT_SHOW_YEAR | DateUtils.FORMAT_SHOW_DATE;
		} else if (then.yearDay != now.yearDay) {
			// If it is from a different day than today, show only the date.
			format_flags |= DateUtils.FORMAT_SHOW_DATE;
		} else {
			// Otherwise, if the message is from today, show the time.
			format_flags |= DateUtils.FORMAT_SHOW_TIME;
		}

		// If the caller has asked for full details, make sure to show the date
		// and time no matter what we've determined above (but still make
		// showing
		// the year only happen if it is a different year from today).
		if (fullFormat) {
			format_flags |= (DateUtils.FORMAT_SHOW_DATE | DateUtils.FORMAT_SHOW_TIME);
		}
		return DateUtils.formatDateTime(context, when, format_flags);
	}

	public static String getDateFromBase(long date) {
		Time time = new Time();
		time.set(date);
		String minute = "";
		if (time.minute < 10) {
			minute = "0" + time.minute;
		}
		String result = time.year + "年" + (time.month + 1) + "月" + time.monthDay + "日" + time.hour + "：" + minute;
		return result;
	}
}
